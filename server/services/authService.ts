/**
 * Configuration OpenID Connect mise en cache
 * Le cache √©vite les appels r√©p√©t√©s au serveur d'autorisation
 */
const getOidcConfig = memoize(
  async () => {
    console.log("üîê Initialisation configuration OpenID Connect...");
    return await client.discovery(
      new URL(authConfig.issuerUrl),
      authConfig.replId
    );
  },
  { maxAge: 3600 * 1000 } // Cache pendant 1 heure
);

/**
 * Configuration s√©curis√©e des sessions
 * Utilise PostgreSQL pour la persistance en production
 */
export function createSessionMiddleware() {
  const sessionTtl = 7 * 24 * 60 * 60 * 1000; // 1 semaine
  
  // Configuration du store de session PostgreSQL
  const pgStore = connectPg(session);
  const sessionStore = new pgStore({
    conString: databaseConfig.url,
    createTableIfMissing: false, // La table sessions existe d√©j√† dans le sch√©ma
    ttl: sessionTtl,
    tableName: "sessions",
  });

  console.log("üõ°Ô∏è Configuration sessions s√©curis√©es activ√©e");

  return session({
    secret: authConfig.sessionSecret,
    store: sessionStore,
    resave: false,
    saveUninitialized: false,
    cookie: {
      httpOnly: true,
      secure: !isDevelopment, // HTTPS obligatoire en production
      maxAge: sessionTtl,
      sameSite: "lax", // Protection CSRF
    },
    name: "nurselink.sid", // Nom personnalis√© pour la s√©curit√©
  });
}

/**
 * Met √† jour les donn√©es de session utilisateur avec les nouveaux tokens
 */
function updateUserSession(
  user: any,
  tokens: client.TokenEndpointResponse & client.TokenEndpointResponseHelpers
) {
  user.claims = tokens.claims();
  user.access_token = tokens.access_token;
  user.refresh_token = tokens.refresh_token;
  user.expires_at = user.claims?.exp;
  
  if (isDevelopment) {
    console.log("üîÑ Session utilisateur mise √† jour:", {
      userId: user.claims?.sub,
      expiresAt: new Date(user.expires_at * 1000).toISOString(),
    });
  }
}

/**
 * Cr√©e ou met √† jour le profil utilisateur lors de l'authentification
 */
async function upsertUserProfile(claims: any) {
  try {
    await storage.upsertUser({
      id: claims["sub"],
      email: claims["email"],
      firstName: claims["first_name"],
      lastName: claims["last_name"],
      profileImageUrl: claims["profile_image_url"],
    });
    
    console.log("üë§ Profil utilisateur synchronis√©:", {
      userId: claims["sub"],
      email: claims["email"] ? "***@***.***" : null,
    });
  } catch (error) {
    console.error("‚ùå Erreur synchronisation profil utilisateur:", error);
    throw error;
  }
}

/**
 * Configuration compl√®te de l'authentification OAuth
 */
export async function setupAuthentication(app: Express) {
  console.log("üöÄ Initialisation du syst√®me d'authentification...");

  // Configuration Express pour les proxies (Replit/production)
  app.set("trust proxy", 1);

  // Middleware de session
  app.use(createSessionMiddleware());

  // Middleware d'authentification locale pour le d√©veloppement
  app.use(localAuthMiddleware);

  // Si mode d√©veloppement local, on configure les routes locales et on arr√™te ici
  if (authConfig.localDevMode) {
    console.log("üîß Mode d√©veloppement local activ√© - Contournement OAuth Replit");
    setupLocalAuthRoutes(app);
    console.log("üîê Syst√®me d'authentification local configur√© avec succ√®s");
    return;
  }

  // Initialisation Passport (seulement en mode production/Replit)
  app.use(passport.initialize());
  app.use(passport.session());

  // Configuration OpenID Connect
  const config = await getOidcConfig();

  /**
   * Fonction de v√©rification Passport
   * Appel√©e lors de chaque authentification r√©ussie
   */
  const verify: VerifyFunction = async (
    tokens: client.TokenEndpointResponse & client.TokenEndpointResponseHelpers,
    verified: passport.AuthenticateCallback
  ) => {
    try {
      const user = {};
      updateUserSession(user, tokens);
      await upsertUserProfile(tokens.claims());
      verified(null, user);
    } catch (error) {
      console.error("‚ùå Erreur lors de la v√©rification OAuth:", error);
      verified(error, false);
    }
  };

  // Configuration des strat√©gies pour chaque domaine Replit
  for (const domain of authConfig.domains) {
    const strategy = new Strategy(
      {
        name: `replitauth:${domain}`,
        config,
        scope: "openid email profile offline_access",
        callbackURL: `https://${domain}/api/auth/callback`,
      },
      verify,
    );
    
    passport.use(strategy);
    console.log(`‚úÖ Strat√©gie OAuth configur√©e pour: ${domain}`);
  }

  // S√©rialisation/d√©s√©rialisation des sessions
  passport.serializeUser((user: Express.User, cb) => cb(null, user));
  passport.deserializeUser((user: Express.User, cb) => cb(null, user));

  // Routes d'authentification
  setupAuthRoutes(app);

  console.log("üîê Syst√®me d'authentification configur√© avec succ√®s");
}

/**
 * Configuration des routes d'authentification
 */
function setupAuthRoutes(app: Express) {
  // Route de connexion
  app.get("/api/auth/login", (req, res, next) => {
    const strategy = `replitauth:${req.hostname}`;
    passport.authenticate(strategy, {
      prompt: "login consent",
      scope: ["openid", "email", "profile", "offline_access"],
    })(req, res, next);
  });

  // Route de callback OAuth
  app.get("/api/auth/callback", (req, res, next) => {
    const strategy = `replitauth:${req.hostname}`;
    passport.authenticate(strategy, {
      successReturnToOrRedirect: "/",
      failureRedirect: "/api/auth/login",
    })(req, res, next);
  });

  // Route de d√©connexion
  app.get("/api/auth/logout", async (req, res) => {
    try {
      const config = await getOidcConfig();
      req.logout(() => {
        res.redirect(
          client.buildEndSessionUrl(config, {
            client_id: authConfig.replId,
            post_logout_redirect_uri: `${req.protocol}://${req.hostname}`,
          }).href
        );
      });
    } catch (error) {
      console.error("‚ùå Erreur lors de la d√©connexion:", error);
      res.redirect("/");
    }
  });

  // Route pour r√©cup√©rer les informations utilisateur
  app.get("/api/auth/user", requireAuthentication, async (req: any, res) => {
    try {
      const userId = req.user.claims.sub;
      const user = await storage.getUser(userId);
      
      if (!user) {
        return res.status(404).json({ 
          error: "Utilisateur non trouv√©",
          code: "USER_NOT_FOUND" 
        });
      }

      res.json(user);
    } catch (error) {
      console.error("‚ùå Erreur r√©cup√©ration utilisateur:", error);
      res.status(500).json({ 
        error: "Erreur serveur",
        code: "INTERNAL_ERROR" 
      });
    }
  });
}

/**
 * Middleware d'authentification requis
 * Compatible avec le mode d√©veloppement local et OAuth Replit
 */
export const requireAuthentication: RequestHandler = async (req, res, next) => {
  // En mode d√©veloppement local, on utilise l'utilisateur fictif
  if (authConfig.localDevMode) {
    (req as any).user = {
      claims: {
        sub: "local-dev-user-123",
        email: "dev@nurselink.local",
        name: "D√©veloppeur Local",
        given_name: "D√©veloppeur",
        family_name: "Local"
      }
    };
    return next();
  }

  const user = req.user as any;

  // V√©rification de l'authentification de base
  if (!req.isAuthenticated() || !user?.expires_at) {
    return res.status(401).json({ 
      error: "Non authentifi√©",
      code: "UNAUTHORIZED" 
    });
  }

  const now = Math.floor(Date.now() / 1000);
  
  // Token encore valide
  if (now <= user.expires_at) {
    return next();
  }

  // Tentative de refresh du token
  const refreshToken = user.refresh_token;
  if (!refreshToken) {
    return res.status(401).json({ 
      error: "Session expir√©e",
      code: "SESSION_EXPIRED" 
    });
  }

  try {
    console.log("üîÑ Refresh automatique du token d'acc√®s...");
    const config = await getOidcConfig();
    const tokenResponse = await client.refreshTokenGrant(config, refreshToken);
    
    updateUserSession(user, tokenResponse);
    
    console.log("‚úÖ Token d'acc√®s renouvel√© avec succ√®s");
    return next();
  } catch (error) {
    console.error("‚ùå Erreur lors du refresh du token:", error);
    return res.status(401).json({ 
      error: "Impossible de renouveler la session",
      code: "REFRESH_FAILED" 
    });
  }
};

/**
 * Middleware d'authentification optionnel
 * N'interrompt pas la requ√™te si l'utilisateur n'est pas connect√©
 */
export const optionalAuthentication: RequestHandler = async (req, res, next) => {
  if (!req.isAuthenticated()) {
    return next();
  }

  // Appliquer la m√™me logique que requireAuthentication mais sans bloquer
  try {
    await new Promise<void>((resolve, reject) => {
      requireAuthentication(req, res, (err) => {
        if (err) reject(err);
        else resolve();
      });
    });
  } catch (error) {
    // En cas d'erreur, on continue sans authentification
    req.logout?.(() => {});
  }

  next();
};

/**
 * Utilitaire pour extraire les informations utilisateur de la requ√™te
 */
export function getUserFromRequest(req: any): { userId: string; claims: any } | null {
  if (!req.isAuthenticated() || !req.user?.claims?.sub) {
    return null;
  }

  return {
    userId: req.user.claims.sub,
    claims: req.user.claims,
  };
}